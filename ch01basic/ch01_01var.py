# a = 100
# b = 50
# input() 사용자에게 문자열로 데이터를 입력받는다.

a = int(input("첫 번째 숫자를 입력해 주세요 : "))
b = int(input("두 번째 숫자를 입력해 주세요 : "))

result = a + b
print(a, '+', b, '=', result)

result = a - b
print(a, '-', b, '=', result)

result = a * b
print(a, '*', b, '=', result)

result = a / b
print(a, '/', b, '=', result)
