# 이미지에 사용할 거북이 이미지 라이브러리
import turtle

# shape : 형태
turtle.shape("turtle")

# forward : 앞으로 전진
# right : 방향전환

turtle.forward(100)
turtle.right(60)
turtle.forward(100)
turtle.right(60)
turtle.forward(100)
turtle.right(60)
turtle.forward(100)
turtle.right(60)
turtle.forward(100)
turtle.right(60)
turtle.forward(100)
turtle.right(60)

# 이미지 창이 닫히지 않고 계속 유지된다.
turtle.done
